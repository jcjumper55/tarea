    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row=tblResultado.getSelectedRow();
        if (row != -1){
            String ruta=tblResultado.getModel().getValueAt(row, 0).toString();
            String fecha=tblResultado.getModel().getValueAt(row, 1).toString();
            String hora=tblResultado.getModel().getValueAt(row, 2).toString();
            String iden=tblResultado.getModel().getValueAt(row, 3).toString();
            String[] num=tblResultado.getModel().getValueAt(row, 8).toString().split(":");
            
            Boletos dis=new Boletos();
            dis.readData();
            objects.Boleto bol=dis.SearchOne(ruta, hora, fecha, iden);
            if(bol!=null){
                //System.out.println(bol.getAsientosStr());
                String[] asientosStr=bol.getAsientosStr().split(";");
                if(asientosStr.length==1){
                    System.out.println("Eliminando Registro");
                    dis.deleteData(bol);
                }else{
                    String asientosNew="";
                    Double total=bol.getTotal();
                    for(int i=0;i<asientosStr.length;i++){
                         String[] asientoNum=asientosStr[i].split(":");
                         if(num[0].equals(asientoNum[0])){
                             total-=(Double.parseDouble(asientoNum[2]));
                         }else{
                             asientosNew+=(("".equals(asientosNew)?"":";")+asientosStr[i]);
                         }
                    }
                    
                    bol.setAsientosStr(asientosNew);
                    bol.setTotal(total);
                    System.out.println("Eliminando asiento Registro");
                    dis.updateData(bol);
                }
                modelos.Disponibilidad dispo=new Disponibilidad();
                dispo.readData();
                objects.Disponibilidad disponible=dispo.Search(ruta, hora, fecha);
                disponible.setOcupado(Integer.parseInt(num[0]), false);
                dispo.updateData(disponible);
                this.search();
            }else
                JOptionPane.showMessageDialog(null, "No se encontro el boleto a eliminar!", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);            
        }else
            JOptionPane.showMessageDialog(null, "Debe Seleccionar una el asiento a eliminar!", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_jButton3ActionPerformed

