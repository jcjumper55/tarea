    private void search(){
        Boletos dis=new Boletos();
        dis.readData();  
        String fecha=((JTextField)dtpFecha.getDateEditor().getUiComponent()).getText();
        if(!"".equals(fecha.trim())){           
            boletos=dis.Search(cmbRuta.getSelectedItem().toString(),cmbHora.getSelectedItem().toString(), ((JTextField)dtpFecha.getDateEditor().getUiComponent()).getText());
            if(boletos.size()>0){
                Object [][] tabla=new Object [boletos.size()][9];
                int i=0;
                for(objects.Boleto bol:boletos){
                    tabla[i]=new Object[]{ bol.getParadaInicial()+" - "+bol.getParadaFinal(), bol.getFecha(), bol.getHora(), bol.getIdentificacion(), bol.getApellidos(), bol.getNombres(), bol.getEdad(), bol.getValor(), bol.getAsiento() };
                    i++;
                }
                tblResultado.setModel(new javax.swing.table.DefaultTableModel(tabla,labelsTtbl));
            }else this.clearTable();
        } else this.clearTable();
    }
